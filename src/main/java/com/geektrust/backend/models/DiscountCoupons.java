package com.geektrust.backend.models;

/**
 * Enum to represent various types of discount coupons available in the system.
 * B4G1: Buy 4 get 1 free
 * DEAL_G20: Get 20% discount on the total bill
 * DEAL_G5: Get 5% discount on the total bill
 * NONE: No discount coupon applied
 */

public enum DiscountCoupons {
    B4G1, DEAL_G20, DEAL_G5, NONE
}
